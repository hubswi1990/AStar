	private void drawScene(ActionEvent e) {

		Scene map = new Scene(20, 25, sceneTab);
		AStarHeuristics heuristics = new Euklides();
		AStar astar = new AStar(map, heuristics);
		astar.calculate(0, 20, 19, 15);

		Graphics gr = getGraphics();

		for (int x = 0; x < sceneTab.length; x++) {
			for (int y = 0; y < sceneTab[x].length; y++) {
				
				if (map.getNode(x, y).isObstacle())
					gr.setColor(Color.blue);
				else if (map.getNode(x, y).isStart())
					gr.setColor(Color.green);
				else if (map.getNode(x, y).isEnd())
					gr.setColor(Color.red);
				else if (map.getNode(x, y).isPath())
					gr.setColor(Color.yellow);
				else
					gr.setColor(Color.black);
				gr.fillRect(20 + (y * 20), 80 + (x * 20), 20, 20);
				gr.setColor(Color.white);
				gr.drawRect(20 + (y * 20), 80 + (x * 20), 20, 20);
			}
		}
	}
